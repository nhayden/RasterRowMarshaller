void IntegrationTest::testRealWorld_GrahicData() {
    vector<BYTE> buf;

    RasterRow scratchRaster( (vector<BYTE>(_documentRasterRowWidthInBytes)));
    for (int i = 0; i < _rows; ++i) {
        ByteArray graphicData(_ppData[i], _graphicByteWidth);
        scratchRaster.WriteIntoRasterAtPosition(graphicData, _graphicXPos);
        vector<BYTE> compressed = scratchRaster.Compress();
//        cout << i << ": ";
//        for (const BYTE &b : compressed) {
//            cout << std::hex;
//            cout << std::setw(2) << std::setfill('0');
//            cout << (signed)b << " ";
//        }
//        cout << endl;
        buf.insert(buf.end(), compressed.begin(), compressed.end());
        scratchRaster.ZeroOutData();
    }
    PadPackedBits(buf);
    
    const vector<BYTE> firstBox {0xe8, 0x00, 0x02, 0x07, 0xff, 0x80, 0xd5, 0x00};
    const vector<BYTE> blankRow {0xb9, 0x00};
    const vector<BYTE> secondBox{0xd9, 0x00, 0x02, 0x03, 0xff, 0xc0, 0xe4, 0x00};
    vector<BYTE> ans;
    // first box is 12 lines long
    for (int i = 0; i < 12; ++i) ans.insert(end(ans), begin(firstBox), end(firstBox));
    // 32 blank rows between boxes
    for (int i = 0; i < 32; ++i) ans.insert(end(ans), begin(blankRow), end(blankRow));
    // second box is also 12 lines long
    for (int i = 0; i < 12; ++i) ans.insert(end(ans), begin(secondBox), end(secondBox));
    
    CPPUNIT_ASSERT(buf == ans);
}

