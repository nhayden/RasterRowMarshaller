void print2dvec(std::vector<std::vector<BYTE>> &vv) {
    for (int i = 0; i < vv.size(); ++i) {
        std::cout << std::endl << i << ": ";
        for (int j = 0; j < vv[i].size(); ++j) {
          std::cout << (int)vv[i][j] << " ";
        }
  }
}

